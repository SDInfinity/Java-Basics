package Operators;

public class relationaloperator                    //gives boolean as return type
{
    public static void main(String[] args) {
        int a = 10;                               //Relational operators return a boolean value and compares 2 values
        int b = 11;
        System.out.println(a > b);                //greater than
        System.out.println(a < b);                //less than
        System.out.println(a >= b);               //greater than equal to
        System.out.println(a <= b);               //less than equal to
        System.out.println(a == b);               //equality
        System.out.println(a!=b);                 //not equal to
    }
}
